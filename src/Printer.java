
public class Printer {
	
	// 메소드 오버로딩 구현
	public void println(int num) {
		System.out.println(num);
	}
	public void println(Boolean num) {
		System.out.println(num);
	}
	public void println(double num) {
		System.out.println(num);
	}
	public void println(String num) {
		System.out.println(num);
	}
	
}
